class OprahsFavoriteThings::Product
  attr_accessor :title, :description, :price, :image, :retailer, :url

  def initialize args = {}
    @title = args[:title]
    @description = args[:description]
    @price = args[:price]
    @image = args[:image]
    @retailer = args[:retailer]
    @url = args[:url]
  end

  def attributes
    {
      title: @title,
      description: @description,
      price: @price,
      image: @image,
      retailer: @retailer,
      url: @url
    }
  end
end
